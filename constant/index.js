 function updateDomainCounts(mentors, domain) {
 
  const domainCountMap = new Map();

 
  domain.forEach(d => domainCountMap.set(d.name, 0));

 
  mentors.forEach(mentor => {
    const domainName = mentor.domain;
    if (domainCountMap.has(domainName)) {
      domainCountMap.set(domainName, domainCountMap.get(domainName) + 1);
    } else {
     
      domainCountMap.set(domainName, 1);
    }
  });

  
  domain.forEach(d => {
    if (domainCountMap.has(d.name)) {
      d.count = domainCountMap.get(d.name);
    }
  });

 
  domainCountMap.forEach((count, name) => {
    if (!domain.some(d => d.name === name)) {
      domain.push({ name, count });
    }
  });
}


updateDomainCounts(mentors, domain);



